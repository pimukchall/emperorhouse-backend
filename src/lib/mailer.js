import nodemailer from "nodemailer";

let _tx = null;

function bool(v, def = false) {
  if (v === undefined) return def;
  return v === true || v === "true" || v === "1";
}

async function createTransporter() {
  // ‡πÉ‡∏ä‡πâ SMTP ‡∏à‡∏£‡∏¥‡∏á‡∏ñ‡πâ‡∏≤‡∏°‡∏µ ENV
  if (process.env.SMTP_HOST && process.env.SMTP_USER && process.env.SMTP_PASS) {
    return nodemailer.createTransport({
      host: process.env.SMTP_HOST,
      port: Number(process.env.SMTP_PORT || 587),
      secure: bool(process.env.SMTP_SECURE, false), // true = 465
      auth: {
        user: process.env.SMTP_USER,
        pass: process.env.SMTP_PASS,
      },
      pool: true,
    });
  }

  // Dev: ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ö‡∏±‡∏ç‡∏ä‡∏µ Ethereal ‡∏≠‡∏±‡∏ï‡πÇ‡∏ô‡∏°‡∏±‡∏ï‡∏¥
  const test = await nodemailer.createTestAccount();
  return nodemailer.createTransport({
    host: "smtp.ethereal.email",
    port: 587,
    secure: false,
    auth: { user: test.user, pass: test.pass },
  });
}

export async function getTransporter() {
  if (_tx) return _tx;
  _tx = await createTransporter();
  return _tx;
}

export async function sendMail({ to, subject, html, text }) {
  const tx = await getTransporter();
  const from =
    process.env.MAIL_FROM ||
    `"${process.env.APP_NAME || "EMP One"}" <no-reply@example.com>`;
  const info = await tx.sendMail({ from, to, subject, html, text });
  const preview = nodemailer.getTestMessageUrl(info);
  if (preview) {
    console.log(`üìß [Ethereal preview] ${preview}`);
  }
  return info;
}

